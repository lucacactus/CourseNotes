#include<iostream>
using namespace std;
int main(){
	int integerVar = 30;
	float floatingVar = 33.79;
	double doubleVar = 8.44e+3;
	char charVar = 'W';
	bool boolVar = true;
	cout<<"integerVar = "<<integerVar<<endl;
	cout<<"floatingVar  = "<<floatingVar<<endl;
	cout<<"doubleVar  = "<<doubleVar<<endl;
	cout<<"charVar = "<<charVar<<endl;
	cout<<"boolVar = "<<boolVar<<endl;
	return 0;
}